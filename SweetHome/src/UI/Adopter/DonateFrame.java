    public boolean isAmountValidate(float amount) {
        String input = donation.getText();
        try {

            float donationamount = Float.parseFloat(input);
            if (donationamount > 0) {
                // Input is valid,
                return true;
            } else {

                return false;
            }
        } catch (NumberFormatException e) {
            // Input is not a valid float
            return false;
        }
    }

