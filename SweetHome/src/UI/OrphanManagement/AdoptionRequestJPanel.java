    private void btnApproveAdoptionRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApproveAdoptionRequestActionPerformed
        int selectedRow = tblAnimalAdoptionWorkQueue.getSelectedRow();
        if (selectedRow >= 0) {

            WorkRequest request = (WorkRequest) tblAnimalAdoptionWorkQueue.getValueAt(selectedRow, 0);
            if (request.getReceiver() == null) {
                JOptionPane.showMessageDialog(this, "Request has not been received.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (!request.getReceiver().equals(this.useraccount)) {
                JOptionPane.showMessageDialog(this, "Request has already been received by other authority.",
                        "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (request.getStatus().equalsIgnoreCase("Rejected")) {
                JOptionPane.showMessageDialog(this, "Request has already been rejected!",
                        "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (request.getStatus().equalsIgnoreCase("Approved")) {
                JOptionPane.showMessageDialog(this, "Request has already been approved!",
                        "Warning", JOptionPane.WARNING_MESSAGE);

            } else if (request.getReceiver() != null && request.getReceiver().equals(this.useraccount) && (!request.getStatus().equalsIgnoreCase("Rejected")) && (!request.getStatus().equalsIgnoreCase("Approved"))) {
                for (WorkRequest wr : this.useraccount.getNetwork().getWorkQueue().getWorkRequestList()) {
                    if (wr.getOrphan().equals(request.getOrphan()) && (!wr.getSender().equals(request.getSender())) && wr.getStatus().equalsIgnoreCase("Approved")) {
                        request.setStatus("Rejected");
                        populateRequestTable();
                        JOptionPane.showMessageDialog(this, "This orphan has been adopted already", "Warning", JOptionPane.WARNING_MESSAGE);
                        break;
                    }

                }

                if (request.getStatus().equalsIgnoreCase("Rejected")) {

                } else {
                    request.setStatus("Approved");
                    request.getOrphan().setAdoptor(request.getSender());
                    populateRequestTable();
                    JOptionPane.showMessageDialog(this, "Approved successfully.", "Information", JOptionPane.INFORMATION_MESSAGE);

                }

            } else {

                request.setStatus("Approved");
                populateRequestTable();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please choose first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnApproveAdoptionRequestActionPerformed

