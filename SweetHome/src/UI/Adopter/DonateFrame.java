    private void btnDonateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDonateActionPerformed
        try {
            String input = donation.getText();
            float donationamount = Float.parseFloat(input);
            
            if (input != null) {
                
                if (isAmountValidate(donationamount)) {
                    Float donationAmount = Float.valueOf(donation.getText());
                    this.enterprise.addDonation(donationAmount);
                    JOptionPane.showMessageDialog(this, "Thank you for your support for our Enterprise!.", "Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Please enter the correct amount", "Warning", JOptionPane.WARNING_MESSAGE);
                }
                
            } else {
                JOptionPane.showMessageDialog(this, "The amount can not be null", "Warning", JOptionPane.WARNING_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter the correct content", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        System.out.println(this.enterprise.getDonation());
               

    }//GEN-LAST:event_btnDonateActionPerformed

