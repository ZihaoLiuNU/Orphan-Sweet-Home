    private void btnReceiveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReceiveActionPerformed
        try {
            int selectedRow = tblAnimalAdoptionWorkQueue.getSelectedRow();
            WorkRequest request = (WorkRequest) tblAnimalAdoptionWorkQueue.getValueAt(selectedRow, 0);

            if (selectedRow >= 0) {

                if (request.getReceiver() != null) {

                    JOptionPane.showMessageDialog(this, "Request has already been received.", "Information", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    request.setReceiver(this.useraccount);
                    request.setStatus("Processed");

                    populateRequestTable();
                }

            } else {
                JOptionPane.showMessageDialog(this, "Please select a row.",
                        "Warning", JOptionPane.WARNING_MESSAGE);

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please select a row.",
                    "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnReceiveActionPerformed

    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) tblAnimalAdoptionWorkQueue.getModel();

        model.setRowCount(0);
        for (WorkRequest request : network.getWorkQueue().getWorkRequestList()) {
            if (request instanceof AdoptionRequest /*&& request.getOrphan() == orphan*/) {
                Object[] row = new Object[6];
                row[0] = request;
                row[1] = request.getOrphan();
                row[2] = request.getSender().getEmailId();
                row[3] = request.getSender().getOrgainization();
                row[4] = request.getReceiver() == null ? null : request.getReceiver();
                row[5] = request.getStatus();

                model.addRow(row);
            }
        }
    }

